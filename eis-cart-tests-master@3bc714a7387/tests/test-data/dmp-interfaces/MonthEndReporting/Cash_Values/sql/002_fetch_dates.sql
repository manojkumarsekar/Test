SELECT TO_CHAR(LAST_DAY(TRUNC(SYSDATE)) + 1,'YYYY-MON-DD') AS MS_DATE,
       TO_CHAR(ADD_MONTHS(LAST_DAY(TRUNC(SYSDATE)), 1),'YYYY-MON-DD') AS ME_DATE,
       TO_CHAR(NEXT_DAY(LAST_DAY(TRUNC(SYSDATE)) + 1, 'Monday'),'YYYY-MON-DD') AS TRD_DATE,
       TO_CHAR(NEXT_DAY(LAST_DAY(TRUNC(SYSDATE)) + 1, 'Tuesday'),'YYYY-MON-DD') AS TRD_DATE_2,
       TO_CHAR(NEXT_DAY(LAST_DAY(TRUNC(SYSDATE)) + 1, 'Wednesday'),'YYYY-MON-DD') AS TRD_DATE_3,
       TO_CHAR(NEXT_DAY(LAST_DAY(TRUNC(SYSDATE)) + 1, 'Monday'),'YYYYMMDD') AS OUTPUT_TRD_DATE,
       TO_CHAR(NEXT_DAY(LAST_DAY(TRUNC(SYSDATE)) + 1, 'Tuesday'),'YYYYMMDD') AS OUTPUT_TRD_DATE_2,
       TO_CHAR(NEXT_DAY(LAST_DAY(TRUNC(SYSDATE)) + 1, 'Wednesday'),'YYYYMMDD') AS OUTPUT_TRD_DATE_3,
       TO_CHAR(LAST_DAY(TRUNC(SYSDATE)) + 1,'YYYYMMDD') AS OUTPUT_MS_DATE,
       TO_CHAR(NEXT_DAY(LAST_DAY(TRUNC(SYSDATE)) + 1, 'Monday'),'dd-Mon-yyyy') AS TRD_DATE_OUTPUT,
       TO_CHAR(GREATEST(LAST_DAY(TRUNC(SYSDATE)) + 1, NEXT_DAY(LAST_DAY(TRUNC(SYSDATE)) + 1 - 5, 'Monday')),'YYYYMMDD') AS FIRST_BUS_DATE,
       TO_CHAR(ADD_MONTHS(LAST_DAY(TRUNC(SYSDATE)), 1),'YYYYMMDD') AS ME_DATE_OUTPUT,
       MAX(usr_int_val_num_1) + 1 BATCH_NUMBER
FROM   ft_t_wtrd
WHERE  version_end_tmsmp > ADD_MONTHS(SYSDATE, -3)