SELECT CASE WHEN (CASE WHEN TRD_LEGEND_TXT IS NULL THEN ' ' ELSE TRD_LEGEND_TXT END)=
(CASE WHEN '${VAR_NOTES}' ='null' THEN ' ' ELSE '${VAR_NOTES}' END) THEN 'PASS' ELSE 'FAIL' END AS TRD_LEGEND_TXT_CHECK, TRD_LEGEND_TXT AS ACTUAL,'${VAR_NOTES}' AS EXPECTED
FROM FT_T_EXTR
WHERE EXEC_TRD_ID =
(
    SELECT EXEC_TRD_ID FROM FT_T_ETID
    WHERE EXEC_TRN_ID_CTXT_TYP ='BNPTRNEVID' AND EXEC_TRN_ID = '${VAR_BNP_SOURCE_TRAN_EV_ID}' AND trunc(LAST_CHG_TMS)=trunc(SYSDATE)
)