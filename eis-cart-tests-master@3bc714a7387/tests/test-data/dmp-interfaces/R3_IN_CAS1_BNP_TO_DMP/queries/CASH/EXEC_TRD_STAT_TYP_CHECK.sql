WITH
TABL_1 AS
(
    SELECT EXEC_TRD_STAT_TYP FROM FT_T_EXST
    WHERE EXEC_TRD_ID =
        (
            SELECT EXEC_TRD_ID FROM FT_T_ETID
            WHERE EXEC_TRN_ID_CTXT_TYP ='BNPTRNEVID' AND EXEC_TRN_ID = '${VAR_BNP_SOURCE_TRAN_EV_ID}' AND trunc(LAST_CHG_TMS)=trunc(SYSDATE)
        )
)SELECT EXEC_TRD_STAT_TYP, CASE WHEN EXEC_TRD_STAT_TYP = (CASE WHEN '${VAR_CANCEL_IND}' = 'N' THEN 'NEWM' ELSE 'CANC' END) THEN 'PASS' ELSE 'FAIL' END AS EXEC_TRD_STAT_TYP_CHECK
FROM TABL_1