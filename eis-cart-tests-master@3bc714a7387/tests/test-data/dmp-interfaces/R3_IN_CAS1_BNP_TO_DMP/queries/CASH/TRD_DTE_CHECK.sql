    WHERE EXEC_TRN_ID_CTXT_TYP ='BNPTRNEVID' AND EXEC_TRN_ID = '${VAR_BNP_SOURCE_TRAN_EV_ID}' AND trunc(LAST_CHG_TMS)=trunc(SYSDATE)
