WITH TABL_1 AS
(
    SELECT ACCT_ID AS ACID_ACCT_ID
    FROM FT_T_ACID
    WHERE ACCT_ID_CTXT_TYP='BNPPRTID'
    AND ACCT_ALT_ID = '${VAR_ACCT_ID}'
),
TABL_2 AS
(
    SELECT ACCT_ID AS EXTR_ACCT_ID
    FROM FT_T_EXTR
    WHERE EXEC_TRD_ID IN
    (
        SELECT EXEC_TRD_ID
        FROM FT_T_ETID
        Where EXEC_TRN_ID_CTXT_TYP ='BNPTRNEVID'
        AND EXEC_TRN_ID IN ('${VAR_BNP_SOURCE_TRAN_EV_ID}')
        AND trunc(LAST_CHG_TMS)=trunc(SYSDATE)
    )
)
SELECT CASE WHEN A.ACID_ACCT_ID = B.EXTR_ACCT_ID THEN 'PASS' ELSE 'FAIL' END as ACCT_ID_CHECK,A.ACID_ACCT_ID, B.EXTR_ACCT_ID
FROM TABL_1 A
   CROSS JOIN TABL_2 B