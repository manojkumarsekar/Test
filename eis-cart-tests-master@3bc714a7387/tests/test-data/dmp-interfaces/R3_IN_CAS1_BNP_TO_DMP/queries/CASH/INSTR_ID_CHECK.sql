WITH
TABL_1 AS
(
    SELECT INSTR_ID AS ISID_INSTR_ID FROM FT_T_ISID
    WHERE ID_CTXT_TYP='BNPLSTID'
    AND ISS_ID IN ('${VAR_INSTR_ID}') and END_TMS IS NULL
),
TABL_2 AS
(
    SELECT INSTR_ID AS EXTR_INSTR_ID
    FROM FT_T_EXTR
    WHERE EXEC_TRD_ID IN
    (
        SELECT EXEC_TRD_ID
        FROM FT_T_ETID
        Where EXEC_TRN_ID_CTXT_TYP ='BNPTRNEVID'
        AND EXEC_TRN_ID IN ('${VAR_BNP_SOURCE_TRAN_EV_ID}')
        AND trunc(LAST_CHG_TMS)=trunc(SYSDATE)
    )
)
SELECT CASE WHEN A.ISID_INSTR_ID = B.EXTR_INSTR_ID THEN 'PASS' ELSE 'FAIL' END AS INSTR_ID_CHECK,A.ISID_INSTR_ID,B.EXTR_INSTR_ID
FROM TABL_1 A
    CROSS JOIN TABL_2 B