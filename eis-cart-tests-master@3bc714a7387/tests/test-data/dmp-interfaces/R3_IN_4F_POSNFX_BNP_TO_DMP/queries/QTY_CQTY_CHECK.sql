SELECT CASE WHEN
(CASE WHEN QTY_CQTY IS NULL THEN 0 ELSE QTY_CQTY END)=(CASE WHEN ${VAR_ORIG_QUANTITY} IS NULL THEN (CASE WHEN ${VAR_NOMINAL} IS NULL THEN 0 ELSE ${VAR_NOMINAL} END) ELSE ${VAR_ORIG_QUANTITY} END)
THEN 'PASS' ELSE 'FAIL' END AS QTY_CQTY_CHECK,QTY_CQTY AS ACTUAL,(CASE WHEN ${VAR_ORIG_QUANTITY} IS NULL THEN ${VAR_NOMINAL} ELSE ${VAR_ORIG_QUANTITY} END) AS EXPECTED
FROM FT_T_BALH
WHERE BALH_OID='${BALH_OID}'