WITH
TABL_1 AS
(
    SELECT INSTR_ID AS ISID_INSTR_ID FROM FT_T_ISID
    WHERE ID_CTXT_TYP='BNPLSTID'
    AND ISS_ID IN ('${VAR_INSTR_ID}') and END_TMS IS NULL
),
TABL_2 AS
(
    SELECT INSTR_ID AS BALH_INSTR_ID
    FROM FT_T_BALH
    WHERE BALH_OID='${BALH_OID}'
)
SELECT CASE WHEN A.ISID_INSTR_ID = B.BALH_INSTR_ID THEN 'PASS' ELSE 'FAIL' END AS INSTR_ID_CHECK,A.ISID_INSTR_ID,B.BALH_INSTR_ID
FROM TABL_1 A
    CROSS JOIN TABL_2 B