SELECT CASE WHEN
(CASE WHEN BKPG_CURR_MKT_CAMT IS NULL THEN 0 ELSE BKPG_CURR_MKT_CAMT END)=
(CASE WHEN ${VAR_ACCRUED_INC_P} IS NULL THEN (CASE WHEN ${VAR_VALUATION_P} IS NULL THEN 0 ELSE ${VAR_VALUATION_P} END)
ELSE (CASE WHEN ${VAR_VALUATION_P} IS NULL THEN 0 ELSE ${VAR_VALUATION_P} END) - ${VAR_ACCRUED_INC_P} END)
THEN 'PASS' ELSE 'FAIL' END AS BKPG_CURR_MKT_CAMT_CHECK,BKPG_CURR_MKT_CAMT AS ACTUAL
FROM FT_T_BALH
WHERE BALH_OID='${BALH_OID}'