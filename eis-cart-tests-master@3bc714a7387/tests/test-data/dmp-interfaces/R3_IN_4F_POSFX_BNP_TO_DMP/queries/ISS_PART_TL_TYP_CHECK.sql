SELECT CASE WHEN A.ISS_PART_RL_TYP=(CASE WHEN '${VAR_LONG_SHORT_IND}' = 'L' THEN '1' ELSE '2' END) THEN 'PASS' ELSE 'FAIL' END AS ISS_PART_TL_TYP_CHECK,A.ISS_PART_RL_TYP AS ACTUAL
FROM FT_T_RISS A
    JOIN FT_T_ISID B
        ON A.INSTR_ID = B.INSTR_ID
    JOIN FT_T_RIDF C
        ON A.RLD_ISS_FEAT_ID = C.RLD_ISS_FEAT_ID
    JOIN FT_T_ISID D
        ON C.INSTR_ID = D.INSTR_ID
WHERE B.ISS_ID = '${VAR_TRAN_ID}'
AND B.ID_CTXT_TYP = 'FXTRANID'
AND D.ID_CTXT_TYP = 'BNPLSTID'
AND D.ISS_ID = '${VAR_INSTR_ID}'
AND D.END_TMS IS NULL
